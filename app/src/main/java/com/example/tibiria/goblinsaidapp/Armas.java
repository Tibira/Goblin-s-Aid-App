package com.example.sdf.goblinsaidapp;

public class Armas {
    public String nome;
    public String descricao;
    public String tipo;
    public String preco;
    public int peso;
    public String dano;

    public Armas(String nome, String descricao, String tipo, String preco, int peso, String dano) {
        this.nome = nome;
        this.descricao = descricao;
        this.tipo = tipo;
        this.preco = preco;
        this.peso = peso;
        this.dano = dano;
    }
}
