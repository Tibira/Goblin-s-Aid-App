package com.example.tibiria.goblinsaidapp;

public class Magias {
    public String nome;
    public String descricao;
    public String conjuradores;
    public int nivel;
    public String escola;
    public String tempo;
    public String componentes;
    public String alcance;
    public String duracao;


    public Magias(String nome, String descricao, String conjuradores, int nivel, String escola, String tempo, String componentes, String alcance, String duracao) {
        this.nome = nome;
        this.descricao = descricao;
        this.conjuradores = conjuradores;
        this.nivel = nivel;
        this.escola = escola;
        this.tempo = tempo;
        this.componentes = componentes;
        this.alcance = alcance;
        this.duracao = duracao;
    }
}
